CREATE TABLE AWR_MODEL_RUN (
    MODEL_RUN_ID             NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    SCORING_MODEL_ID         NUMBER,
    SOURCE_SYSTEM_ID         NUMBER,
    AWR_ID                   NUMBER,
    FEATURE_VECTOR_ID        NUMBER,

    RUN_TYPE                 VARCHAR2(30)         NOT NULL,
    RUN_STATUS               VARCHAR2(30)         NOT NULL,
    STARTED_AT               TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    ENDED_AT                 TIMESTAMP(6),

    TRAINING_SET_REF         VARCHAR2(256),
    MODEL_ARTIFACT_REF       VARCHAR2(2048),
    METRICS_JSON             JSON,
    PARAMS_JSON              JSON,
    OUTPUT_JSON              JSON,
    ERROR_JSON               JSON,

    CONSTRAINT FK_AWR_MODEL_RUN_MODEL
        FOREIGN KEY (SCORING_MODEL_ID)
        REFERENCES AWR_SCORING_MODEL (SCORING_MODEL_ID),

    CONSTRAINT FK_AWR_MODEL_RUN_SOURCE
        FOREIGN KEY (SOURCE_SYSTEM_ID)
        REFERENCES AWR_SOURCE_SYSTEM (SOURCE_SYSTEM_ID),

    CONSTRAINT FK_AWR_MODEL_RUN_AWR
        FOREIGN KEY (AWR_ID)
        REFERENCES AWR_REPORT (AWR_ID),

    CONSTRAINT FK_AWR_MODEL_RUN_VECTOR
        FOREIGN KEY (FEATURE_VECTOR_ID)
        REFERENCES AWR_FEATURE_VECTOR (FEATURE_VECTOR_ID),

    CONSTRAINT CK_AWR_MODEL_RUN_TYPE
        CHECK (RUN_TYPE IN ('TRAIN','SCORE','RETRAIN','BACKTEST')),

    CONSTRAINT CK_AWR_MODEL_RUN_STATUS
        CHECK (RUN_STATUS IN ('STARTED','COMPLETED','FAILED','CANCELLED'))
);

