CREATE OR REPLACE VIEW VW_AWR_REPORT_LATEST AS
SELECT
    s.SOURCE_SYSTEM_CODE,
    s.CUSTOMER_NAME,
    s.ENVIRONMENT_NAME,
    r.AWR_ID,
    r.DB_NAME,
    r.DBID,
    r.INSTANCE_NAME,
    r.INSTANCE_NUMBER,
    r.SNAP_ID_BEGIN,
    r.SNAP_ID_END,
    r.SNAP_TIME_BEGIN,
    r.SNAP_TIME_END,
    r.SNAP_ELAPSED_MINUTES,
    r.PARSE_STATUS,
    r.ANOMALY_FLAG,
    r.INGEST_MODE,
    r.SOURCE_FILE_NAME,
    r.CREATED_AT
FROM AWR_REPORT r
JOIN AWR_SOURCE_SYSTEM s
  ON s.SOURCE_SYSTEM_ID = r.SOURCE_SYSTEM_ID
WHERE r.SNAP_TIME_END = (
    SELECT MAX(r2.SNAP_TIME_END)
    FROM AWR_REPORT r2
    WHERE r2.SOURCE_SYSTEM_ID = r.SOURCE_SYSTEM_ID
);

CREATE OR REPLACE VIEW VW_AWR_RECOMMENDATION_CYCLE AS
SELECT
    s.SOURCE_SYSTEM_CODE,
    r.AWR_ID,
    r.SNAP_TIME_END,
    sr.SCORE_RESULT_ID,
    sr.DECISION_DOMAIN,
    sr.TOTAL_SCORE,
    sr.RISK_LEVEL,
    rec.RECOMMENDATION_ID,
    rec.RECOMMENDATION_TYPE,
    rec.STATUS AS RECOMMENDATION_STATUS,
    act.ACTION_ID,
    act.ACTION_TYPE,
    act.ACTION_STATUS,
    o.OUTCOME_ID,
    o.OUTCOME_STATUS,
    o.SUCCESS_FLAG
FROM AWR_REPORT r
JOIN AWR_SOURCE_SYSTEM s
  ON s.SOURCE_SYSTEM_ID = r.SOURCE_SYSTEM_ID
LEFT JOIN AWR_SCORE_RESULT sr
  ON sr.AWR_ID = r.AWR_ID
LEFT JOIN AWR_RECOMMENDATION rec
  ON rec.SCORE_RESULT_ID = sr.SCORE_RESULT_ID
LEFT JOIN AWR_ACTION act
  ON act.RECOMMENDATION_ID = rec.RECOMMENDATION_ID
LEFT JOIN AWR_OUTCOME o
  ON o.ACTION_ID = act.ACTION_ID;

CREATE OR REPLACE VIEW VW_AWR_TOP_SQL_TREND AS
SELECT
    s.SOURCE_SYSTEM_CODE,
    t.SOURCE_SYSTEM_ID,
    t.AWR_ID,
    t.SNAP_TIME_BEGIN,
    t.SNAP_TIME_END,
    t.SQL_ID,
    t.PLAN_HASH_VALUE,
    t.RANK_BY_ELAPSED,
    t.RANK_BY_CPU,
    t.ELAPSED_TIME_SEC,
    t.CPU_TIME_SEC,
    t.IO_TIME_SEC,
    t.EXECUTIONS,
    t.ELAPSED_PER_EXEC_SEC,
    t.CPU_PER_EXEC_SEC
FROM AWR_TOP_SQL_FACT t
JOIN AWR_SOURCE_SYSTEM s
  ON s.SOURCE_SYSTEM_ID = t.SOURCE_SYSTEM_ID;

CREATE OR REPLACE VIEW VW_AWR_WAIT_EVENT_TREND AS
SELECT
    s.SOURCE_SYSTEM_CODE,
    w.SOURCE_SYSTEM_ID,
    w.AWR_ID,
    w.SNAP_TIME_BEGIN,
    w.SNAP_TIME_END,
    w.WAIT_CLASS,
    w.EVENT_NAME,
    w.RANK_IN_AWR,
    w.TOTAL_WAITS,
    w.TIME_WAITED_SEC,
    w.AVG_WAIT_MS,
    w.PCT_DB_TIME
FROM AWR_WAIT_EVENT_FACT w
JOIN AWR_SOURCE_SYSTEM s
  ON s.SOURCE_SYSTEM_ID = w.SOURCE_SYSTEM_ID;

